// 2. ФУНКЦИЯ ОТРИСОВКИ
const container = document.getElementById('ranking-container');

function renderRanking() {
  container.innerHTML = teamsData.map(team => `
    <div class="hltv-wrapper">
      <div class="hltv-card" onclick="toggleCard(this)">
        <div class="rank">${team.rank}</div>
        <img src="${team.logo}" class="team-mini-logo" onerror="this.src='logo1.png'">
        <div class="team-info">
          <div class="team-title">${team.name} <span class="points">(${team.points} point)</span></div>
          <div class="team-players">${team.playersString}</div>
        </div>
        <div class="expand-icon">+</div>
      </div>

      <div class="team-details">
        <div class="players-grid">
          <!-- ГЕНЕРАЦИЯ ИНДИВИДУАЛЬНЫХ ИГРОКОВ -->
          ${team.playersList.map(player => `
            <div class="player-item">
              <div class="img-container">
                <img src="${player.photo}" alt="${player.name}" onerror="this.src='logo1.png'">
              </div>
              <div class="player-meta">
                <span>${player.name}</span>
              </div>
            </div>
          `).join('')}
        </div>
        </div>
      </div>
    </div>
  `).join('');
}

// 3. ЛОГИКА РАСКРЫТИЯ
function toggleCard(element) {
  const wrapper = element.closest('.hltv-wrapper');
  if (wrapper) {
    wrapper.classList.toggle('active');
    const icon = element.querySelector('.expand-icon');
    icon.textContent = wrapper.classList.contains('active') ? '−' : '+';
  }
}

document.addEventListener('DOMContentLoaded', renderRanking);
